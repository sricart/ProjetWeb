                <h1> Support </h1>

                <br>
                <br>

                Bienvenue sur CESI ton stage ! Nous sommes là pour vous aider à naviguer à travers le processus de demande de stage et à répondre à toutes vos questions. Voici un support d'aide qui vous guidera tout au long de votre parcours.

                <br>
                <br>

                <h3> Comment puis-je m'inscrire sur le site ? </h3> 

                <br>
                <br>
                
                Pour vous inscrire sur notre site, rendez-vous sur la page d'accueil et cliquez sur le bouton "S'inscrire". Vous devrez ensuite fournir vos informations de base telles que votre nom, votre adresse e-mail et un mot de passe.

                <br>
                <br>

                <h3> Comment puis-je rechercher des offres de stage ? </h3> 

                <br>
                <br>

                Une fois que vous êtes inscrit, vous pouvez rechercher des offres de stage en utilisant le champ de recherche sur la page d'accueil. Vous pouvez également parcourir les offres de stage en cliquant sur le lien "Offres de stage" dans le menu de navigation.

                <br>
                <br>

                Comment puis-je postuler à un stage ?

                <br>
                <br>

                Pour postuler à un stage, vous devez d'abord vous connecter à votre compte. Ensuite, rendez-vous sur la page de l'offre de stage que vous souhaitez postuler et cliquez sur le bouton "Postuler". Vous devrez ensuite remplir un formulaire de demande en fournissant vos informations personnelles et votre CV.

                <h3> Comment puis-je suivre l'état de ma demande de stage ? </h3> 

                <br>
                <br>

                Une fois que vous avez postulé à un stage, vous pouvez suivre l'état de votre demande en vous connectant à votre compte et en accédant à la section "Mes demandes de stage". Vous y trouverez des mises à jour sur l'état de votre demande, telles que si elle est en attente d'examen ou si elle a été acceptée.

                <br>
                <br>
                
                <h3> Comment puis-je suivre l'état de ma demande de stage ? </h3> 

                <br>
                <br>
                Si vous avez des questions sur une offre de stage ou sur le processus de demande, vous pouvez contacter l'employeur en utilisant le bouton "Contacter l'employeur" sur la page de l'offre de stage. Vous pouvez également envoyer un e-mail à notre équipe de support à l'adresse indiquée sur notre site.

                <br>
                <br>

                Nous espérons que ce support d'aide vous aidera à naviguer facilement sur notre site et à postuler à des offres de stage. Si vous avez d'autres questions, n'hésitez pas à nous contacter. Bonne chance pour votre recherche de stage !






                <h2>
                Qui sommes-nous ?
                </h2>
        
                École d’ingénieurs créée en 1958 par des entreprises industrielles, CESI compte aujourd’hui 26 000 étudiants au travers de 25 campus sur tout le territoire, dotés d’équipements pédagogiques de pointe, 95 000 alumni, 8 000 entreprises partenaires, 130 partenariats avec des universités dans le monde.
               
                <br>
                <br>

                CESI a développé un modèle unique au sein de l’enseignement supérieur. Notre école d’ingénieurs accompagne ses étudiants dans les secteurs
                
                <br>
                <br>
                de l’Industrie,
                
                <br>
                du BTP,
                
                <br>
                et de l’Informatique & du Numérique, 
                <br>
                <br>
                à travers son offre de formation composée de ses programmes : Grande École, Grade de Licence, Professionnel Supérieur de bac +2 à bac +5, Mastère Spécialisé®, Doctoral, Executive et Passerelles. Convaincue de la nécessité de concilier sciences, technologies et sciences humaines, CESI se distingue également par une offre de formation en Ressources Humaines & Management.
                <br>
                <br>
                Sa gouvernance est composée de dirigeants d’entreprise et de cinq branches professionnelles : UIMM (Union des Industries et Métiers de la Métallurgie), FFB (Fédération Française du Bâtiment), FNTP (Fédération Nationale des Travaux Publics), FFIE (Fédération Française des Intégrateurs Electriciens) et Numeum.
                <br>
                <br>
                L’école d’ingénieurs CESI, pionnière dans le secteur de l’enseignement supérieur technique privé et dans la formation professionnelle, est présente sur tout le territoire français grâce à ses 25 campus qui dispensent des parcours sur-mesure et délivrent les mêmes diplômes.

